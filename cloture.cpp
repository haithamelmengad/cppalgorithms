bool binaire (Carte const& carte){
    bool binaire = true;
    for( size_t i = 0; i<carte.size(); i++){
        for ( size_t j = 0; j<carte[i].size(); j++){
            if (carte[i][j] != 1 && carte[i][j] != 0){
                binaire = false;
                break;
            }
        }
    }
    return binaire;
}

bool verifie_et_modifie(Carte& carte){
    bool zerosnones = binaire(carte);
    if( zerosnones == false){
        cout << "Votre carte du terrain ne contient pas que des 0 et des 1." << endl;
        return zerosnones;
    } else {
        for( size_t i = 0; i<carte.size(); i++){
            size_t line_size = carte[i].size();
            size_t j = 0;
            while(carte[i][j] != 1){
                j++;
            }
            while(carte[i][line_size-1] != 1){
                line_size--;
            }
            for( size_t j_ones = j; j_ones<line_size; j_ones++){
                carte[i][j_ones] = 1;
            }
        }
        return zerosnones;
    }
}

double longueur_cloture(Carte const& carte, double echelle = 2.5) {
    double la_longueur = 0;
    for( size_t i = 0; i<carte.size(); i++){
        for ( size_t j = 0; j<carte[i].size(); j++){
            if(j == 0){
                if ( carte[i][j] == 1){
                    la_longueur += echelle;
                }
            } else {
                if ((carte[i][j-1] == 0 && carte[i][j] == 1)){
                    la_longueur += echelle;
                }
            }
            if (j == carte[i].size()-1){
                if (carte[i][j] == 1){
                    la_longueur += echelle;
                }
            } else {
                if(carte[i][j+1] == 0 && carte[i][j] == 1){
                    la_longueur += echelle;
                }
            }
            if (i==0){
                if (carte[i][j] == 1){
                    la_longueur += echelle;
                }
            } else {
                if (carte[i-1][j] == 0 && carte[i][j] == 1){
                    la_longueur += echelle;
                }
            }
            if( i == carte.size()-1){
                if (carte[i][j] == 1){
                    la_longueur += echelle;
                }
            } else {
                if(carte[i+1][j] == 0 && carte[i][j] == 1){
                    la_longueur += echelle;
                }
            }
        }
    }
    return la_longueur;
}

